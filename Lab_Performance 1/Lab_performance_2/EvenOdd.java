import java.lang.*;

public class EvenOdd
{
    public static void main(String[] args) 
    {
        int number=40;
        if(number %2==0)
        {
            System.out.println(number+" is a Even Number");
        }
        
        else
        {
            System.out.println(number+" is a Odd Number");
        }
    }
}